SELECT AD_CHANGWA, AD_AMPHOE, MUNISAN_ID, MS_NAME, ZONE
FROM TDASSET_FORMER.tdadmin.MUNISAN_47
    WHERE 1=1
    AND AD_CHANGWA = @AD_CHANGWA
    AND AD_AMPHOE = @AD_AMPHOE
    AND MUNISAN_ID = @MUNISAN_ID
UNION ALL
SELECT AD_CHANGWA, AD_AMPHOE, MUNISAN_ID, MS_NAME, ZONE
FROM TDASSET_FORMER.tdadmin.MUNISAN_48
    WHERE 1=1
    AND AD_CHANGWA = @AD_CHANGWA
    AND AD_AMPHOE = @AD_AMPHOE
    AND MUNISAN_ID = @MUNISAN_ID
GROUP BY AD_CHANGWA, AD_AMPHOE, MUNISAN_ID, MS_NAME, ZONE
ORDER BY AD_CHANGWA, AD_AMPHOE, MUNISAN_ID, MS_NAME, ZONE