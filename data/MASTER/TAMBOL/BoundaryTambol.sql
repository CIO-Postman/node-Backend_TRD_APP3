SELECT PRO_C, ON_PRO_THA, DIS_C,  ON_DIS_THA, SUB_C, ON_SUB_THA, ZONE
  FROM TDASSET_FORMER.tdadmin.TAMBOL_47
  WHERE 1=1
  AND PRO_C =@PRO_C
  AND DIS_C = @DIS_C
  AND SUB_C = @SUB_C
UNION ALL
