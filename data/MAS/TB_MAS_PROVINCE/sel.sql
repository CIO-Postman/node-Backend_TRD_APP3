SELECT CHANGWAT_CODE, CHANGWAT_NAME, 
  AMPHUR_CODE, AMPHUR_NAME, 
  OPT_CODE, OPT_NAME 
FROM DB3.dbo.TD_ATTACH_OPT
WHERE 1=1
--   AND CHANGWAT_CODE = '44'
--   AND AMPHUR_CODE = '08'
--   AND OPT_CODE = '05440801'
GROUP BY CHANGWAT_CODE, CHANGWAT_NAME, 
  AMPHUR_CODE, AMPHUR_NAME, 
  OPT_CODE, OPT_NAME 
ORDER BY CHANGWAT_CODE, CHANGWAT_NAME, 
  AMPHUR_CODE, AMPHUR_NAME, 
  OPT_CODE, OPT_NAME 